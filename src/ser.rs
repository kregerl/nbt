pub fn to_writer<T, W>(w: W, value: &T) -> error::Result<()>
where
    T: Serialize,
    W: io::Write,
{
    let serializer = NBTSerializer::new(w);
    value.serialize(serializer)
}

struct NBTSerializer<W> {
    writer: W
}

impl<W: io::Write> NBTSerializer<W> {
    pub fn new(writer: W) -> Self {
        Self {
            writer,
        }
    }
    fn write_tag_header(&mut self, kind: NBTKind, name: Option<&str>) -> error::Result<()> {
        self.writer.write_u8(kind.header_byte())?;
        if let Some(name) = name {
            self.writer.write_u16::<byteorder::BigEndian>(name.len() as u16)?;
            self.writer.write(name.as_bytes())?;
        }
        Ok(())
    }
}

